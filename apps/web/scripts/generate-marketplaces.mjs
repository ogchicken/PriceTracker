// Generates src/lib/api/marketplaces.ts from docs/marketplaces.json, which the
// API produces from its store adapters. Mirrors `generate:api`: the artefact is
// committed and CI fails if it drifts from the source.
//
// The generated module has to live inside apps/web because the web Dockerfile
// only copies apps/web into the build context — docs/ is not available there.
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const source = resolve(here, "../../../docs/marketplaces.json");
const target = resolve(here, "../src/lib/api/marketplaces.ts");

const marketplaces = JSON.parse(readFileSync(source, "utf8"));
const names = Object.keys(marketplaces).sort();

const quoted = (values) => values.map((value) => `"${value}"`).join(", ");

const entries = names
  .map((name) => {
    const { domains, hostPrefixes } = marketplaces[name];
    return [
      `  ${name}: {`,
      `    domains: [${quoted(domains)}],`,
      `    hostPrefixes: [${quoted(hostPrefixes)}]`,
      `  }`
    ].join("\n");
  })
  .join(",\n");

const contents = `// Generated by \`pnpm generate:marketplaces\` from docs/marketplaces.json.
// Do not edit by hand: the supported hosts are defined once, in the API's store
// adapters (apps/api/app/providers/adapters.py). Editing here would let the
// browser accept URLs the API rejects. Regenerate instead.

export type SupportedMarketplace = ${names.map((name) => `"${name}"`).join(" | ")};

export interface MarketplaceHostRules {
  readonly domains: readonly string[];
  readonly hostPrefixes: readonly string[];
}

export const MARKETPLACES: Readonly<Record<SupportedMarketplace, MarketplaceHostRules>> = {
${entries}
} as const;

export const SUPPORTED_MARKETPLACES = Object.keys(MARKETPLACES) as SupportedMarketplace[];
`;

mkdirSync(dirname(target), { recursive: true });
writeFileSync(target, contents, "utf8");
console.log(`Wrote ${target}`);
